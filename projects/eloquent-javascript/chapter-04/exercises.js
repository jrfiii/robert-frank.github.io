// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  let output = [];
  if (start === end) {
    return output;
  } else if (step > 0) {
    for (var i = start; i <= end; i += step) {
      output.push(i);
    }
  } else if (step < 0) {
    for (var i = start; i >= end; i += step) {
      output.push(i);
    }
  } else {
    for (var i = start; i <= end; i++) {
      output.push(i);
    }
  }
  return output;
}

